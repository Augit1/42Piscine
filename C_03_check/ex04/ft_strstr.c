char	*ft_strstr(char *str, char *to_find)
{
	const char	*i;
	const char	*j;

	while (*str)
	{
		i = str;
		j = to_find;
		while (*j && *i == *j)
		{
			i++;
			j++;
		}
		if (!*j)
			return ((char *) str);
		str++;
	}
	return (0);
}
