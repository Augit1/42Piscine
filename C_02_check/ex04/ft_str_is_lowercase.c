int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			j++;
		}
		i++;
	}
	if (i != j)
	{
		return (0);
	}
	return (1);
}
